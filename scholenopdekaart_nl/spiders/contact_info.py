    def parse(self, response):
        # Select the <dl> element containing the information
        dl_element = response.css('dl.dl-horizontal.dl-icons')

        # Extract phone number, school website, and email address
        phone_number = dl_element.css('dt:contains("Telefoon:") + dd a::attr(href)').get()
        school_website = dl_element.css('dt:contains("Schoolwebsite:") + dd a::attr(href)').get()
        email_address = dl_element.css('dt:contains("E-mailadres:") + dd a::attr(href)').get()

        # Clean the extracted data
        if phone_number:
            phone_number = phone_number.replace('tel:', '')
        if school_website:
            school_website = school_website.strip()
        if email_address:
            email_address = email_address.replace('mailto:', '')

        # Yield the extracted data
        yield {
            'Phone Number': phone_number,
            'School Website': school_website,
            'Email Address': email_address
        }